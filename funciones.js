// hay 2 tipos de fucniones en JS Declarativas y de Expresión

// Declarativas
function miFuncion() {
    return 3;
}
miFuncion(); //llamado de la funcion


// Expresión
var miFuncion = function(a, b) { //(a, b) son los parametros que recibe la funcion
    return a + b;
}
miFuncion(); //llamado de la funcion